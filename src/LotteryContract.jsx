// สร้าง Web3 instance
export const getWeb3 = () => {
	if (window.ethereum) {
	  const web3 = new Web3(window.ethereum);
	  return web3;
	}
	throw new Error("ไม่พบ Ethereum provider");
  };
  
  // ดึง contract instance (จะต้องใส่ contractAddress ด้านนอกหากเรียกใช้ในฟังก์ชันนี้)
  export const getContract = (web3, contractAddress) => {
	return new web3.eth.Contract(contractABI, contractAddress);
  };
  
  // ฟังก์ชันล็อกอินเพื่อขอเชื่อมต่อกระเป๋าเงินและคืนค่าบัญชี
  export const login = async () => {
	try {
	  const web3 = getWeb3();
	  // ขออนุญาตให้ผู้ใช้เชื่อมต่อกับ wallet
	  const accounts = await web3.eth.requestAccounts();
	  if (accounts.length > 0) {
		return accounts[0]; // คืนค่าบัญชีแรกที่เชื่อมต่อสำเร็จ
	  } else {
		throw new Error("ไม่พบบัญชี โปรดเชื่อมต่อกระเป๋าเงินของคุณ");
	  }
	} catch (error) {
	  console.error("ข้อผิดพลาดระหว่างการล็อกอิน:", error);
	  throw error;
	}
  };